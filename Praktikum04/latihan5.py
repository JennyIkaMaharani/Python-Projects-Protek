#membuat diagram batang horizontal
jumlahMhsLaki= int(input("Masukkan jumlah mahasiswa laki-laki: "))
jumlahMhsPerempuan= int(input("Masukkan jumlah mahasiswa perempuan: "))
lakiLaki= "*" * jumlahMhsLaki
perempuan= "*" * jumlahMhsPerempuan
print("Laki- laki  :", lakiLaki, "(",jumlahMhsLaki,")")
print("Perempuan   :", perempuan, "(",jumlahMhsPerempuan,")")
